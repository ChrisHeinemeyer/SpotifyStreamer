package com.plusgaurav.spotifystreamer;

public class TrackListData {

    String trackName;
    String trackAlbum;
    String trackImageSmall;
    String trackImageLarge;
    String trackPreviewUrl;

    public String getTrackName() {
        return trackName;
    }

    public void setTrackName(String trackName) {
        this.trackName = trackName;
    }

    public String getTrackAlbum() {
        return trackAlbum;
    }

    public void setTrackAlbum(String trackAlbum) {
        this.trackAlbum = trackAlbum;
    }

    public String getTrackImageSmall() {
        return trackImageSmall;
    }

    public void setTrackImageSmall(String trackImageSmall) {
        this.trackImageSmall = trackImageSmall;
    }

    public String getTrackImageLarge() {
        return trackImageLarge;
    }

    public void setTrackImageLarge(String trackImageLarge) {
        this.trackImageLarge = trackImageLarge;
    }

    public String getTrackPreviewUrl() {
        return trackPreviewUrl;
    }

    public void setTrackPreviewUrl(String trackPreviewUrl) {
        this.trackPreviewUrl = trackPreviewUrl;
    }


}
